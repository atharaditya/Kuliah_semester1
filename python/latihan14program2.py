def insertion_sort(data):
 # perulangan pertama
 for i in range(1, len(data)):
    # ini elemen yang akan kita posisikan
    key_item = data[i]
# kunci index posisi
 j = i - 1
  # lakukan perulangan kedua
 while j >= 0 and data[j] > key_item:
    # menggeser elemen yang lain
    data[j + 1] = data[j]
    j -= 1
    # memposisikan elemen
    data[j + 1] = key_item
 return data

