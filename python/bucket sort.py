def bucketSort(array):
    largest = max(array)
    length = len(array)
    size = largest/length

    # Buat Sebuah larik untuk menaruh bucket-bucket dengan panjang sebanyak 
    # panjang dari larik yang berisikan elemen-elemen yang tidak tersortir atau random
    buckets = [[] for i in range(length)]

    # Bucket Sorting nya 
    for i in range(length):
        index = int(array[i]/size)
        if index != length:
            buckets[index].append(array[i])
        else:
            buckets[length - 1].append(array[i])

    # Lalu Tentukan berapa jumlah bucket yang akan dibentuk dengan kriteria tertentu, misalnya nilai terbesar di dalam larik input dibagi dengan panjang dari larik input  
    for i in range(len(array)):
        buckets[i] = sorted(buckets[i])


    # Iterasikan larik bucket dan lihat setiap elemennya, kalau bucketnya tidak kosong, lakukan insertion sort pada bucket tersebut.
    result = []
    for i in range(length):
        result = result + buckets[i]
            
    return result

