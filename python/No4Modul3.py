#menghitung luas & keliling persegi panjang
print("masukan nilai panjang")
panjang = int(input())
print("masukan nilai lebar")
lebar = int(input())
keliling_persegi_panjang = panjang*lebar
lebar_persegi_panjang = 2*(panjang + lebar)
print(f"sebuah persegi panjang memiliki panjang {panjang} cm, lebar {lebar} cm dan memiliki luas sebesar {keliling_persegi_panjang} cm dan keliling {lebar_persegi_panjang}")